
public class Time 
{
	private int hour;
	private int minutes;
	public Time(int hour, int minutes)
	{
		this.hour = hour;
		this.minutes = minutes;
	}
	
	// Getters: dont need them, but thought i'd show i knew how to make getters lol ;)
	
	public int getHour()
	{
		return this.hour;
	}
	public int getMinutes() 
	{
		return this.minutes;
	}
}
